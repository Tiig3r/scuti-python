def info(text=""):
    """
    Display information
    :param text:
    """
    print("[INFO] " + text)


def error(text=""):
    """
    Display an error
    :param text:
    """
    print("[ERROR] " + text)


def line(text=""):
    """
    Display a line without any tag
    :param text:
    """
    print(text)
